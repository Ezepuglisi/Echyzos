export const traductorColores = (color) => {
    switch (color){
        case 'negro':
            return 'black'
        case 'rojo':
            return 'red'
        case 'rosa':
            return 'pink'
        case 'lila':
            return 'purple'
        case 'blanco':
            return 'grey'
        default:
            return ''
    }
}